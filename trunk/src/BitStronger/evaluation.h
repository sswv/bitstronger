///局面评估类（单件模块） 
/*!
　　局面评估就是给棋局打分，识别棋局的好坏，以便在博弈树搜索时作为路径选择条件寻找最佳着法。可行的办法就是对局面进行量化，通过数值评判棋局的好坏。由于评估需要大量的象棋知识，仁者见仁，智者见智，评估函数的设计便成为计算机博弈中最为人性化的部分。一般评估函数要综合考虑以下几个方面：1、棋子价值，2、棋子位置，3、棋子灵活度，4、棋子配合，5、将帅安全等。 

　　外部程序只调用Eval函数，Eval函数内部调用各种子评估函数，加权求和返回给外部程序。
*/
class Evaluation
{
public:
    ///评估当面局面价值
    static int Eval(Board & cur, PLAYER who);
private:
    ///棋子价值评估与子力位置评估
    static int ValueAndPosition(Board & cur, PLAYER who);
    ///棋子灵活性评估
    static int Agility(Board & cur, PLAYER who);
    ///棋子威胁、保护评估
    static int AttackAndProtect(Board & cur, PLAYER who);
    ///棋子配合度评估
    static int Cooperation(Board & cur, PLAYER who);
    ///得到棋子的威胁、保护评估价值 
    inline static int GetAnPScore(BDPOINT point, CHESSMAN chess);
};

